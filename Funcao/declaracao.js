// Função Declaração(Fuction Declaration)
function soma(x, y) {
    return x + y
}
console.log(soma(6, 5))

// Função Expressão(Function Expression)
const sub = function (x, y) {
    return x - y 
}
console.log(sub(8, 5))

// Nome Função Expressão(Named Function Expression)
const mult = function mult(x, y) {
    return x * y
}
console.log(mult(9, 5))
